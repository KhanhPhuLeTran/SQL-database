	-- Tạo hàm tính tổng thành tiền của phiếu nhập bằng cách nhập vào mã phiếu nhập
CREATE FUNCTION TIEN_NHAP(@MANHAP VARCHAR(10))
RETURNS DECIMAL(20,2)
AS
BEGIN
	DECLARE @DGNHAP DECIMAL(20,2), @SLNHAP INT, @THUENHAP FLOAT(10), @TIENNHAP DECIMAL(20,2)
	SET @DGNHAP=(SELECT DGNHAP FROM PHIEUNHAP WHERE PHIEUNHAP.MANHAP=@MANHAP)
	SET @SLNHAP=(SELECT SLNHAP FROM PHIEUNHAP WHERE PHIEUNHAP.MANHAP=@MANHAP)
	SET @THUENHAP=(SELECT THUENHAP FROM PHIEUNHAP WHERE PHIEUNHAP.MANHAP=@MANHAP)
	SET @TIENNHAP=@DGNHAP*@SLNHAP*(1+@THUENHAP)
	RETURN @TIENNHAP
END
GO
	-- Tạo hàm tính tổng thành tiền của phiếu xuất bằng cách nhập vào mã phiếu xuất
CREATE FUNCTION TIEN_XUAT(@MAXUAT VARCHAR(10))
RETURNS DECIMAL(20,2)
AS
BEGIN
	DECLARE @DGXUAT DECIMAL(20,2), @SLXUAT INT, @THUEXUAT FLOAT(10), @TIENXUAT DECIMAL(20,2)
	SET @DGXUAT=(SELECT DGXUAT FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	SET @SLXUAT=(SELECT SLXUAT FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	SET @THUEXUAT=(SELECT THUEXUAT FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	SET @TIENXUAT=@DGXUAT*@SLXUAT*(1+@THUEXUAT)
	RETURN @TIENXUAT
END
GO
	-- Tạo hàm truy vấn thông tin những đơn khách hàng đã đặt bằng cách nhập vào SĐT khách hàng
CREATE FUNCTION DON_KH_DAT(@SDT CHAR(10))
RETURNS TABLE
AS
RETURN (SELECT * FROM PHIEUXUAT WHERE PHIEUXUAT.MAKH IN (SELECT MAKH FROM KHACHHANG WHERE KHACHHANG.SDT=@SDT))
GO
	-- Tạo hàm tính số tiền khách hàng cần phải trả hằng tháng khi trả góp bằng cách nhập vào SĐT khách hàng và mã phiếu xuất
CREATE FUNCTION TIEN_THANG(@SDT CHAR(10),@MAXUAT VARCHAR(10))
RETURNS DECIMAL(20,2)
AS
BEGIN
	DECLARE @MAGOI VARCHAR(10),@LAISUAT FLOAT(10),@TRATRUOC FLOAT(10),@THOIGIAN INT,@TIENXUAT DECIMAL(20,2),@TIENTHANG DECIMAL(20,2)
	SET @MAGOI=(SELECT MAGOI FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	SET @LAISUAT=(SELECT LAISUAT FROM TRAGOP WHERE TRAGOP.MAGOI=@MAGOI)
	SET @TRATRUOC=(SELECT TRATRUOC FROM TRAGOP WHERE TRAGOP.MAGOI=@MAGOI)
	SET @THOIGIAN=(SELECT THOIGIAN FROM TRAGOP WHERE TRAGOP.MAGOI=@MAGOI)
	SET @TIENXUAT=dbo.TIEN_XUAT(@MAXUAT)
	SET @TIENTHANG=((@TIENXUAT*@LAISUAT*@THOIGIAN)+(@TIENXUAT*(1-@TRATRUOC)))/@THOIGIAN
	RETURN @TIENTHANG
END
GO
	-- Tạo hàm kiểm tra khách hàng số tiền còn nợ của khách hàng cách nhập vào SĐT khách hàng và mã phiếu xuất
CREATE FUNCTION TIEN_KH_NO(@SDT CHAR(10),@MAXUAT VARCHAR(10))
RETURNS DECIMAL(20,2)
AS
BEGIN
	DECLARE @MAGOI VARCHAR(10),@TIENTHANG DECIMAL(20,2),@THOIGIAN INT,@NGXUAT DATE,@SOTHANGCONLAI INT
	SET @MAGOI=(SELECT MAGOI FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	BEGIN
		SET @TIENTHANG=dbo.TIEN_THANG(@SDT,@MAXUAT)
		SET @THOIGIAN=(SELECT THOIGIAN FROM TRAGOP WHERE TRAGOP.MAGOI=@MAGOI)
		SET @NGXUAT=(SELECT NGXUAT FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
		SET @SOTHANGCONLAI=DATEDIFF(m,@NGXUAT,GETDATE())-@THOIGIAN
		IF @SOTHANGCONLAI>0
			RETURN @SOTHANGCONLAI*@TIENTHANG
		RETURN 0
	END
END
GO
	-- Tạo hàm kiểm tra xe còn được bảo hành không bằng cách nhập mã phiếu xuất
CREATE FUNCTION BH_XE(@MAXUAT VARCHAR(10))
RETURNS NVARCHAR(50)
AS
BEGIN
	DECLARE @MAXE VARCHAR(10),@MABH VARCHAR(10),@GOIBH NVARCHAR(50),@TGBH INT,@TGBHCON INT,@NGXUAT DATE,@TGBHCON_ VARCHAR(10)
	SET @MAXE=(SELECT MAXE FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	SET @MABH=(SELECT MABH FROM XE WHERE XE.MAXE=@MAXE)
	SET @GOIBH=(SELECT GOIBH FROM BAOHANH WHERE BAOHANH.MABH=@MABH)
	SET @NGXUAT=(SELECT NGXUAT FROM PHIEUXUAT WHERE PHIEUXUAT.MAXUAT=@MAXUAT)
	SET @TGBH=CAST((LEFT(@GOIBH,(CHARINDEX(' ',@GOIBH,1)-1))) AS int)
	SET @TGBHCON=DATEDIFF(M,@NGXUAT,GETDATE())-@TGBH
	SET @TGBHCON_=CAST(@TGBHCON AS VARCHAR(10))
	IF @TGBHCON>0
		RETURN N'Xe còn bảo thành '+@TGBHCON_+' tháng'
	RETURN N'Xe hết bảo hành'
END
GO

-- Tạo store procedure
	-- Khách hàng
	-- Viết thủ tục nhập thông tin khách hàng
CREATE PROCEDURE NHAP_KH(@MAKH VARCHAR(10),@TENKH NVARCHAR(50),@NS DATE,@PHAI NVARCHAR(10),@DIACHI NVARCHAR(100),@SDT CHAR(10),@EMAIL VARCHAR(50))
AS
BEGIN
	IF(dbo.CHECK_KH(@SDT)=N'Khách hàng đã tồn tại')
	BEGIN
		PRINT N'Khách hàng '+@TENKH+N' đã tồn tại'
	END
	ELSE
	BEGIN TRY
		INSERT INTO KHACHHANG(MAKH,TENKH,NS,PHAI,DIACHI,SDT,EMAIL) VALUES (@MAKH,@TENKH,@NS,@PHAI,@DIACHI,@SDT,@EMAIL)
		PRINT N'Thêm khách hàng '+@TENKH+N' thành công'
	END TRY
	BEGIN CATCH
		PRINT N'Thêm khách hàng '+@TENKH+N' không thành công'
	END CATCH
END
GO
	-- Viết thủ tục xóa khách hàng bằng cách nhập vào mã khách hàng
CREATE PROCEDURE XOA_KH(@MAKH VARCHAR(10))
AS
BEGIN
	DECLARE @SDT CHAR(10),@TENKH NVARCHAR(50)
	SET @SDT=(SELECT SDT FROM KHACHHANG WHERE KHACHHANG.MAKH=@MAKH)
	SET @TENKH=(SELECT TENKH FROM KHACHHANG WHERE KHACHHANG.MAKH=@MAKH)
	IF(dbo.CHECK_KH(@SDT)=N'Khách hàng đã tồn tại')
	BEGIN TRY
		DELETE FROM KHACHHANG
		WHERE KHACHHANG.MAKH=@MAKH
		PRINT N'Xóa khách hàng '+@TENKH+' thành công'
	END TRY
	BEGIN CATCH
		PRINT N'Xóa khách hàng '+@TENKH+' không thành công'
	END CATCH
	ELSE
	BEGIN
		PRINT N'Khách hàng '+@TENKH+' không tồn tại'
	END
END
