-- Tạo các bảng dữ liệu
	-- 1.Khách hàng
CREATE TABLE KHACHHANG
(
	MAKH VARCHAR(10) NOT NULL PRIMARY KEY,
	TENKH NVARCHAR(50),
	NS DATE CHECK(NS<GETDATE()),
	PHAI NVARCHAR(10) CHECK(PHAI IN (N'Nam',N'Nữ')),
	DIACHI NVARCHAR(100),
	SDT CHAR(10) UNIQUE CHECK(SDT LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]'),
	EMAIL VARCHAR(50)
)
GO
	-- 2.Nhà sản xuất
CREATE TABLE NSX 
(
	MANSX VARCHAR(10) NOT NULL PRIMARY KEY,
	TENNSX NVARCHAR(50),
	DIACHI NVARCHAR(100),
	SDT CHAR(10) UNIQUE CHECK(SDT LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]'),
	EMAIL VARCHAR(50)
)
GO
	-- 3.Bảo hành
CREATE TABLE BAOHANH 
(
	MABH VARCHAR(10) NOT NULL PRIMARY KEY,
	GOIBH NVARCHAR(50)
)
GO
	-- 4.Loại xe
CREATE TABLE LOAIXE 
(
	MALOAI VARCHAR(10) NOT NULL PRIMARY KEY,
	TENLOAI NVARCHAR(50) UNIQUE
)
GO
	-- 5.Xe
CREATE TABLE XE 
(
	MAXE VARCHAR(10) NOT NULL PRIMARY KEY,
	TENXE NVARCHAR(50),
	MALOAI VARCHAR(10),
	MABH VARCHAR(10),
	BAN NVARCHAR(50) CHECK (BAN IN (N'Tiêu chuẩn',N'Cao cấp',N'Đặc biệt',N'Cá tính',N'Thể thao',N'Vành đúc phanh đĩa',N'Vành nan hoa phanh cơ',N'Vành nan hoa phanh đĩa')),
	MAU NVARCHAR(50),
	GIA DECIMAL(20,2) CHECK(GIA>0),
	SL INT CHECK(SL>=0),
	FOREIGN KEY (MALOAI) REFERENCES LOAIXE(MALOAI),
	FOREIGN KEY (MABH) REFERENCES BAOHANH(MABH)
)
GO
	-- 6.Nhân viên
CREATE TABLE NHANVIEN
(
	MANV VARCHAR(10) NOT NULL PRIMARY KEY,
	TENNV NVARCHAR(50),
	NS DATE CHECK(NS<GETDATE()),
	PHAI NVARCHAR(10) CHECK(PHAI IN (N'Nam',N'Nữ')),
	DIACHI NVARCHAR(100),
	SDT CHAR(10) UNIQUE CHECK(SDT LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]'),
	CHUCVU NVARCHAR(50),
	LUONG DECIMAL(20,2) CHECK (LUONG >0),
	MANQL VARCHAR(10),
	FOREIGN KEY (MANQL) REFERENCES NHANVIEN(MANV)
)
GO
	-- 7.Trả góp
CREATE TABLE TRAGOP
(
	MAGOI VARCHAR(10) NOT NULL PRIMARY KEY,
	TRATRUOC FLOAT(10),
	LAISUAT FLOAT(10),
	THOIGIAN INT CHECK (THOIGIAN>0)
)
GO
	-- 8.Phiếu nhập
CREATE TABLE PHIEUNHAP
(
	MANHAP VARCHAR(10) NOT NULL PRIMARY KEY,
	NGNHAP DATE CHECK (NGNHAP <= GETDATE()),
	MANV VARCHAR(10),
	MANSX VARCHAR(10),
	MAXE VARCHAR(10),
	SLNHAP INT CHECK(SLNHAP >=1),
	DGNHAP DECIMAL(20,2) CHECK(DGNHAP >0),
	THUENHAP FLOAT(10) CHECK(THUENHAP >=0 AND THUENHAP<=1),
	FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
	FOREIGN KEY (MANSX) REFERENCES NSX(MANSX),
	FOREIGN KEY (MAXE) REFERENCES XE(MAXE)
)
GO
	-- 9.Phiếu xuất
CREATE TABLE PHIEUXUAT
(
	MAXUAT VARCHAR(10) NOT NULL PRIMARY KEY,
	NGXUAT DATE CHECK (NGXUAT <= GETDATE()),
	MANV VARCHAR(10),
	MAKH VARCHAR(10),
	MAXE VARCHAR(10),
	SLXUAT INT CHECK(SLXUAT >=1),
	DGXUAT DECIMAL(20,2) CHECK (DGXUAT>0),
	THUEXUAT FLOAT(10) CHECK(THUEXUAT >=0 AND THUEXUAT<=1),
	PTTT NVARCHAR(50) CHECK (PTTT IN (N'Chuyển khoản', N'Tiền mặt', N'Thẻ ngân hàng', N'Thẻ Visa, Master Card')),
	MAGOI VARCHAR(10),
	FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
	FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH),
	FOREIGN KEY (MAXE) REFERENCES XE(MAXE),
	FOREIGN KEY (MAGOI) REFERENCES TRAGOP(MAGOI)
)
GO
	-- Ràng buộc SL trong bảng XE sẽ thêm một lượng đúng bằng SLNHAP trong bảng PHIEUNHAP khi thêm mới một PHIEUNHAP
CREATE TRIGGER TRG_NHAPHANG 
	ON PHIEUNHAP
	FOR DELETE
AS
BEGIN
	UPDATE XE
	SET SL=SL+(
		SELECT SLNHAP
		FROM deleted
		WHERE MAXE=XE.MAXE)
	FROM XE
	JOIN deleted ON XE.MAXE=deleted.MAXE
END
GO
	-- Ràng buộc SL trong bảng XE sẽ giảm một lượng đúng bằng SLXUAT trong bảng PHIEUXUAT khi thêm mới một PHIEUXUAT
CREATE TRIGGER TRG_XUATHANG 
	ON PHIEUXUAT
	AFTER INSERT
AS
BEGIN
	UPDATE XE
	SET SL=SL-(
		SELECT SLXUAT
		FROM inserted
		WHERE MAXE=XE.MAXE)
	FROM XE
	JOIN inserted ON XE.MAXE=inserted.MAXE
END
GO

-- Tạo hàm
	-- Tạo hàm kiểm tra khách hàng có tồn tại không bằng cách nhập SĐT khách hàng
CREATE FUNCTION CHECK_KH(@SDT CHAR(10))
RETURNS NVARCHAR(30)
AS 
BEGIN
	IF EXISTS(SELECT SDT FROM KHACHHANG WHERE KHACHHANG.SDT=@SDT)
		RETURN N'Khách hàng đã tồn tại'
	RETURN N'Khách hàng không tồn tại'
END
GO
	-- Tạo hàm kiểm tra nhân viên có tồn tại không bằng cách nhập SĐT nhân viên
CREATE FUNCTION CHECK_NV(@SDT CHAR(10))
RETURNS NVARCHAR(30)
AS
BEGIN
	IF EXISTS(SELECT SDT FROM NHANVIEN WHERE NHANVIEN.SDT=@SDT)
		RETURN N'Nhân viên đã tồn tại'
	RETURN N'Nhân viên không tồn tại'
END
GO
	-- Tạo hàm kiểm tra nhà sản xuất có tồn tại không bằng cách nhập SĐT nhà sản xuất
